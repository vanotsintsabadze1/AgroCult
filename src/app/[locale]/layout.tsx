import "./globals.css";
import { cookies } from "next/headers";
import { I18nProviderClient } from "@/locales/client";
import { UserProvider } from "@auth0/nextjs-auth0/client";
import { Signika } from "next/font/google";
import LenisWrapper from "../../components/Animation-Wrappers/LenisWrapper";
import { Toaster } from "react-hot-toast";

export const metadata = {
  title: "Create Next App",
  description: "Generated by create next app",
};

const signika = Signika({
  subsets: ["latin"],
  weight: ["300", "400", "500", "600", "700"],
});

interface Props {
  children: React.ReactNode;
  params: {
    locale: string;
  };
}

export default function RootLayout({ children, params: { locale } }: Props) {
  const theme = cookies().get("theme");

  return (
    <html lang={locale} className={`${theme ? theme.value : ""}`}>
      <UserProvider>
        <body className={`${signika.className} bg-body dark:bg-dark-primary`}>
          <I18nProviderClient locale={locale}>
            <LenisWrapper>{children}</LenisWrapper>
          </I18nProviderClient>

          <Toaster position="top-center" containerClassName="text-[1.3rem]" />
        </body>
      </UserProvider>
    </html>
  );
}
